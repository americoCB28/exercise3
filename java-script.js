const celsius = 10;
const fahrenheit = 50;
const celsiusToFahrenheit = (celsius * 9 / 5) + 32;
const fahrenheitToCelsius = (fahrenheit - 32) * 5 / 9;
const fahrenheitAre = fahrenheit + " degrees Fahrenheit are:";
const fahrenheitCelsius = fahrenheitToCelsius + " degrees Celsius";
const celsiusAre = celsius + " degrees Celsius are:";
