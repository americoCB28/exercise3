const kilometers = 70;
const number = 1.609344;
const kilometersToMiles = kilometers / number;
const milesToKilometers = miles * number;
const kilometersAre = kilometers + " kilometers are:";
const kilometersMiles = kilometersToMiles + " miles";
const milesAre = miles + " miles are:";
const milesKilometers = milesToKilometers + " kilometers";
console.log("Distance conversions");
console.log("-------------------------");
console.log(kilometersAre);
